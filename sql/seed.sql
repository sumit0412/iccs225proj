INSERT INTO availability (room_type_id, available_date, total_rooms, available_rooms, rate, currency_code)
SELECT rt.room_type_id,
       date_series.date,
       rt.total_rooms,
       GREATEST(0, rt.total_rooms - FLOOR(RANDOM() * 3)::INT) as available_rooms, -- Random availability
       rt.base_rate * (0.8 + RANDOM() * 0.4)                  as rate,            -- Rate variation ±20%
       rt.currency_code
FROM room_types rt
         CROSS JOIN generate_series(CURRENT_DATE, CURRENT_DATE + INTERVAL '90 days', '1 day') AS date_series(date);

